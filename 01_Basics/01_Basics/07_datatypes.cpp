#include<iostream>
using namespace std;
int main(){
    cout<<"Datatypes in cpp"<<endl;
    // int , char , float, double, boolean , string
   
    // Numeric Datatypes :- int double float
    // float vs double
    float f = 5.5;
    double d = 5.5;
    cout<<"Size of float : "<<sizeof(f)<<endl;
    cout<<"Size of double : "<<sizeof(d)<<endl;


    //  Boolean data type
    bool new1=true;
    cout<<"Value of bool new1 : "<<new1<<endl;
    // Most values of the boolean variable are true
    // Except for 0, false, and null pointer 

    // Character data type
    char c = 'A';
    cout<<"Size of char : "<<sizeof(c)<<endl;

}